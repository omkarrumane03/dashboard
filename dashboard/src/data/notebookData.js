// These replace all hardcoded tables from v4.
// Charts and derivedKPIs.js should import these instead of static arrays.

export function getOrionPeriodData(pipeline = orionPipeline) {
  return MONTHS.map(month => {
    const rows          = pipeline.filter(r => r.month === month);
    const profilesShared = rows.reduce((s, r) => s + (r.profilesShared || 0), 0);
    const l1Reject       = rows.reduce((s, r) => s + (r.l1Reject      || 0), 0);
    const l2Reject       = rows.reduce((s, r) => s + (r.l2Reject      || 0), 0);
    const selections     = rows.reduce((s, r) => s + (r.selections     ?? 0), 0);
    return {
      period:         month,
      month:          MONTH_TO_YYYY_MM[month],
      profilesShared,
      l1Reject,
      l2Reject,
      rejectionCount: l1Reject + l2Reject,
      selections,
      roles:          rows.length,
    };
  });
}

export function getOrionRolesPerPeriod(pipeline = orionPipeline) {
  return MONTHS.map(month => {
    const rows = pipeline.filter(r => r.month === month);
    return {
      period:            month,
      month:             MONTH_TO_YYYY_MM[month],
      rolesOpened:       rows.length,
      rolesClosedHired:  rows.filter(r => r.status === 'Closed-Hired').length,
      rolesClosedNoHire: rows.filter(r => r.status === 'Closed-No Hire').length,
      rolesOnHold:       rows.filter(r => r.status === 'On Hold').length,
      rolesInProcess:    rows.filter(r => r.status === 'In Process').length,
    };
  });
}

