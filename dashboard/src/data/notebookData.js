// ── Experience bucket helper ──────────────────────────────────────────────────
// Thresholds: 2–4 → Junior | 5–7 → Senior | 8+ → Lead
export function getExperienceBucket(exp) {
  const num = parseInt(exp, 10);
  if (isNaN(num)) return 'Senior'; // fallback
  if (num <= 4) return 'Junior';
  if (num <= 7) return 'Senior';
  return 'Lead';
}

